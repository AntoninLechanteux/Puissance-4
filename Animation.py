import tkinter as tk

root=tk.Tk()
root.geometry("720x480")
root.attributes("-fullscreen", True)
root.config(bg="#3394ff")
root.bind("<Escape>", lambda event: root.destroy())
txt= "Bienvenue sur Puissance-4 !"
index=0
text=""
T1=tk.Label(text=txt, font=("System", 50), fg="White", bg="#3394ff")

def animation():
    global index,text
    if index >= len(txt):
        index=-1
        text=""
        T1.config(text=txt)
    else :
        text=text+txt[index]
        T1.config(text=text)
        index+=1
        root.after(100, animation)
    return
T1.grid()
root.after(50, animation)
root.mainloop()




class popup(tk.Frame):
    def __init__(self, parent, start_pos, end_pos):
        super.__init__(master=parent)

        self.start_pos = start_pos
        self.end_pos = end_pos
        self.width = abs(end_pos-start_pos)

        self.pos = start_pos
        self.in_start_pos = True

        self.place(relx=self.start_pos, rely=0, relwidth=self.width, relheight=1)


